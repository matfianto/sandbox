public class FormatBil {
    public static void main(String[] args) {
        int bil = 249;

        System.out.println("bil = " + bil);
        System.out.printf("Nilai oktal = %o\n", bil);
        System.out.printf("Nilai heksadesimal = %x\n", bil);
        System.out.printf("Nilai heksadesimal = %X\n", bil);
        System.out.printf("Nilai desimal = %d\n", bil);
    }
}
