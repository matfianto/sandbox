public class RataKiri {
    public static void main(String[] args) {
        System.out.println("Rata kanan:");
        System.out.printf("%10s\n", "Andi");
        System.out.printf("%10s\n", "Jamal");
        System.out.printf("%10s\n", "Juita");
        System.out.printf("%10s\n", "Karmila");
        System.out.println("-------------------");

        System.out.println("Rata kiri:");
        System.out.printf("%-10s\n", "Andi");
        System.out.printf("%-10s\n", "Jamal");
        System.out.printf("%-10s\n", "Juita");
        System.out.printf("%-10s\n", "Karmila");
        System.out.println("-------------------");
    }
}
