public class ContohArray {
    public static void main(String[] args) {
        int hasil[] = new int[5]; // Deklarasi array dengan 5 elemen

        // Isi setiap elemen array
        hasil[0] = 70;
 	hasil[1] = 80;
	hasil[2] = 75;
	hasil[3] = 90;
	hasil[4] = 65;

        // Tampilkan isi array
        for (int i = 0; i < 5; i++)
            System.out.println(hasil[i]);
    }
}
