public class IsiVar {
    public static void main(String[] args) {
        int a = 1;   // Konstanta
        int b = 2;   // Konstanta
        int c = b;   // variabel
        double x = Math.sqrt(25); // Metode
        double y = x + 2;    // Ekspresi

        System.out.println("a: " + a);
        System.out.println("b: " + b);
        System.out.println("c: " + c);
        System.out.println("x: " + x);
        System.out.println("y: " + y);
    }
}
