public class KonversiHuruf {
    public static void main(String[] args) {
        String st = "Kucoba dan Kucoba. Akhirnya aku bisa!";
        
        System.out.println("Isi st semula: " + st);

        System.out.println("Hasil toLowerCase: " +         
                           st.toLowerCase());

        System.out.println("Hasil toUpperCase: " + 
                           st.toUpperCase());
        System.out.println("Isi st sekarang: " + st);
    }
}
