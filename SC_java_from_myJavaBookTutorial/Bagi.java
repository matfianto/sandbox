public class Bagi {
    public static void main(String[] args) {
        int a = 7;
        double b = 7;

        System.out.println("a / 2   = " + a / 2);
        System.out.println("b / 2   = " + b / 2);
        System.out.println("a / 2.0 = " + a / 2.0);
        System.out.println("b / 2.0 = " + b / 2.0);
    }
}
